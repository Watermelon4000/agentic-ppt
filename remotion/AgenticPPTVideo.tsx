// ============================================================
const PAPER = '#f5f0e8';
const INK   = '#1a1a1a';
const GREY  = '#888';
const LIGHT_GREY = '#d0ccc4';
const TEAL  = '#4ECDC4';

// ============================================================
// DECORATION COMPONENTS
// ============================================================
const Halftone = () => (
  <div style={{
    position: 'absolute', inset: 0, opacity: 0.04, pointerEvents: 'none',
    backgroundImage: `radial-gradient(circle, ${INK} 1px, transparent 1px)`,
    backgroundSize: '6px 6px',
  }} />
);

const ComicBorder = () => (
  <>
    <div style={{position:'absolute',inset:18,border:`3.5px solid ${INK}`,borderRadius:2,pointerEvents:'none',zIndex:1}} />
    <div style={{position:'absolute',inset:14,border:`1px solid ${LIGHT_GREY}`,pointerEvents:'none',zIndex:1}} />
  </>
);

// ============================================================
// WHITEBOARD OVERLAY
// ============================================================
interface WhiteboardEntry {
  text: string;
  startTime: number;
  duration: number;
  visible: boolean;
}

const Whiteboard: React.FC<{ entries: WhiteboardEntry[]; currentTime: number }> = ({
  entries,
  currentTime,
}) => {
  const visible = entries.filter(
    e => e.visible && currentTime >= e.startTime && currentTime < e.startTime + e.duration
  );

  if (visible.length === 0) return null;

  return (
    <div style={{
      position: 'absolute',
      top: 20, right: 20,
      width: 320,
      background: '#fff',
      border: `2.5px solid ${INK}`,
      borderRadius: 4,
      padding: '16px 20px',
      boxShadow: `4px 4px 0 ${INK}`,
      zIndex: 80,
    }}>
      <div style={{
        fontFamily: "'Permanent Marker', cursive",
        fontSize: 13,
        color: GREY,
        letterSpacing: 1,
        marginBottom: 8,
      }}>
        WHITEBOARD
      </div>
      {visible.map((e, i) => {
        // Draw-in animation: 0.5s
        const progress = Math.min(1, (currentTime - e.startTime) / 0.5);
        return (
          <div key={i} style={{
            fontFamily: "'Caveat', cursive",
            fontSize: 22,
            fontWeight: 700,
            color: INK,
            lineHeight: 1.4,
            opacity: progress,
            transform: `translateY(${(1 - progress) * 8}px)`,
            marginBottom: 6,
          }}>
            {e.text}
          </div>
        );
      })}
    </div>
  );
};

