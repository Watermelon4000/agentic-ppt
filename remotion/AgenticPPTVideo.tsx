/**
 * Props injected from Root.tsx or passed at render time.
 * Override with --props flag: npx remotion render AgenticPPTVideo --props='{"layout":"9x16"}'
 */
export interface AgenticPPTProps {
  /** "16x9" or "9x16" */
  layout?: '16x9' | '9x16';
  /** Path to A-roll video in public/ folder */
  arollSrc?: string;
  /** Path to audio narration in public/ */
  audioSrc?: string;
  /** Slide components to render (imported dynamically) */
  slides?: React.FC[];
  /** Action timeline generated by AI */
  actions?: Action[];
}

