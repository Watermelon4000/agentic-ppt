/**
 * AgenticPPTVideo — A-roll + Slides + Whiteboard + Subtitles
 *
 * Usage:
 *   npx remotion render AgenticPPTVideo out/video.mp4
 *   npx remotion render AgenticPPTVideo-9x16 out/video-9x16.mp4
 *
 * Before rendering:
 *   cp /path/to/your-aroll.mp4 remotion-board/public/aroll.mp4
 *   cp /path/to/your-audio.m4a remotion-board/public/narration.m4a
 *   Edit ACTIONS below (or generate with scripts/generate-actions.md prompt)
 *
 * Action types (see types.ts for full definition):
 *   advance_slide — Switch to slide N at startTime seconds
 *   spotlight     — Highlight an element for a duration
 *   wb_write      — Draw whiteboard text
 *   wb_clear      — Clear the whiteboard
 *   subtitle      — Show subtitle text
 *   transition    — Animate slide entrance: 'fade' | 'slide-left' | 'slide-up'
 *   zoom          — Zoom into a target area with a given scale
 */
export const AgenticPPTVideo: React.FC<AgenticPPTProps> = ({
  layout = '16x9',
  arollSrc = 'aroll.mp4',
  audioSrc = 'narration.m4a',
  slides = [],
  actions = [],
}) => {
  const frame = useCurrentFrame();
  const { fps } = useVideoConfig();
  const t = frame / fps;

  // ── Determine current slide from advance_slide actions ──────
  let currentSlideIdx = 0;
  for (const a of actions) {
    if (a.type === 'advance_slide' && t >= a.startTime) {
      currentSlideIdx = a.slideIdx;
    }
  }

  const SlideComponent = slides[currentSlideIdx]
    ? slides[currentSlideIdx]
    : () => <PlaceholderSlide idx={currentSlideIdx} />;

  // ── Whiteboard entries (wb_write actions) ───────────────────
  const wbEntries: WhiteboardEntry[] = actions
    .filter(a => a.type === 'wb_write')
    .map(a => {
      const wa = a as { type: 'wb_write'; text: string; startTime: number; duration: number };
      return {
        text: wa.text,
        startTime: wa.startTime,
        duration: wa.duration,
        visible: t >= wa.startTime && t < wa.startTime + wa.duration,
      };
    });

  // ── Spotlight ───────────────────────────────────────────────
  const spotlightAction = actions.find(a =>
    a.type === 'spotlight' &&
    t >= (a as any).startTime &&
    t < (a as any).startTime + (a as any).duration
  ) as any;
  const spotlightOpacity = spotlightAction
    ? interpolate(t, [spotlightAction.startTime, spotlightAction.startTime + 0.3], [0, 1], { extrapolateLeft: 'clamp', extrapolateRight: 'clamp' })
    : 0;

  // ── Current subtitle ────────────────────────────────────────
  const subtitleAction = actions.find(a =>
    a.type === 'subtitle' &&
    t >= (a as any).startTime &&
    t < (a as any).startTime + (a as any).duration
  ) as any;
  const subtitleText = subtitleAction?.text ?? '';

  // ── Transition animation ────────────────────────────────────
  const transitionAction = actions.find(a =>
    a.type === 'transition' &&
    t >= a.startTime &&
    t < a.startTime + a.duration
  ) as Extract<Action, { type: 'transition' }> | undefined;

  let transitionStyle: React.CSSProperties = {};
  if (transitionAction) {
    const progress = interpolate(
      t,
      [transitionAction.startTime, transitionAction.startTime + transitionAction.duration],
      [0, 1],
      { extrapolateLeft: 'clamp', extrapolateRight: 'clamp', easing: Easing.out(Easing.cubic) }
    );
    switch (transitionAction.style) {
      case 'fade':
        transitionStyle = { opacity: progress };
        break;
      case 'slide-left':
        transitionStyle = { transform: `translateX(${(1 - progress) * 100}%)` };
        break;
      case 'slide-up':
        transitionStyle = { transform: `translateY(${(1 - progress) * 100}%)` };
        break;
    }
  }

  // ── Zoom animation ──────────────────────────────────────────
  const zoomAction = actions.find(a =>
    a.type === 'zoom' &&
    t >= a.startTime &&
    t < a.startTime + a.duration
  ) as Extract<Action, { type: 'zoom' }> | undefined;

  let zoomStyle: React.CSSProperties = {};
  if (zoomAction) {
    const half = zoomAction.duration / 2;
    const scale = t < zoomAction.startTime + half
      ? interpolate(
          t,
          [zoomAction.startTime, zoomAction.startTime + half],
          [1, zoomAction.scale],
          { extrapolateLeft: 'clamp', extrapolateRight: 'clamp', easing: Easing.out(Easing.cubic) }
        )
      : interpolate(
          t,
          [zoomAction.startTime + half, zoomAction.startTime + zoomAction.duration],
          [zoomAction.scale, 1],
          { extrapolateLeft: 'clamp', extrapolateRight: 'clamp', easing: Easing.inOut(Easing.cubic) }
        );
    zoomStyle = { transform: `scale(${scale})` };
  }

  const slideNode = (
    <div style={{ ...transitionStyle, ...zoomStyle, width: '100%', height: '100%', display: 'flex', alignItems: 'center', justifyContent: 'center' }}>
      <SlideComponent />
    </div>
  );

  return (
    <AbsoluteFill style={{ fontFamily: "'Noto Sans SC', sans-serif", backgroundColor: PAPER, color: INK }}>
      <Halftone />

      {layout === '16x9' ? (
        <Layout16x9
          slideComponent={slideNode}
          arollSrc={arollSrc}
          whiteboardEntries={wbEntries}
          currentTime={t}
        />
      ) : (
        <Layout9x16
          slideComponent={slideNode}
          arollSrc={arollSrc}
          whiteboardEntries={wbEntries}
          currentTime={t}
        />
      )}

      {spotlightAction && (
        <Spotlight label={spotlightAction.elementLabel} opacity={spotlightOpacity} />
      )}

      <SubtitleBar text={subtitleText} />

      {audioSrc && <Audio src={staticFile(audioSrc)} />}
    </AbsoluteFill>
  );
};

export default AgenticPPTVideo;
